/*program to demonstrate variables and datatypes
 * and assigning values to variables
 */

package adlin;

public class VariablesMain {
	public static void main(String[] args){
		int firstnum=10; //first num is variable of type integer
		float secondnum=56.76f;
		double thirdnum=987.385;
		char ch='A';
		String s="Edubridge";
		System.out.println("Integer value="+firstnum);
		System.out.println("Float value="+secondnum);
		System.out.println("Double value="+thirdnum);
		System.out.println("Character value="+ch);
		System.out.println("String value="+s);
		
				
	}

}
